package domen;

import java.io.Serializable;

/**
 *
 * @author Mafija
 */
public class Valuta implements Serializable{
    private int id;
    private String naziv;

    public Valuta() {
    }

    public Valuta(int id, String naziv) {
        this.id = id;
        this.naziv = naziv;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return naziv;
    }
    
    
}
